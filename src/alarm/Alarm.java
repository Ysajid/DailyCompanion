    public void soundAlarm(){
           try {
        
               InputStream inputStream = getClass().getResourceAsStream("alert.wav");
               //File file = new File("music/alert.wav");
               //InputStream inputStream = getClass().getResourceAsStream("alert.wav");
             //  URL url = getClass().getResource("/music/alert.wav");
      //  URL url = this.getClass().getClassLoader().getResource("alert.wav");
         AudioInputStream audioIn = AudioSystem.getAudioInputStream(inputStream);
         // Get a sound clip resource.
         Clip clip = AudioSystem.getClip();
         // Open audio clip and load samples from the audio input stream.
         clip.open(audioIn);
         clip.start();
      } catch (UnsupportedAudioFileException e) {
         e.printStackTrace();
      } catch (IOException e) {
         e.printStackTrace();
      } catch (LineUnavailableException e) {
         e.printStackTrace();
      }

    }
